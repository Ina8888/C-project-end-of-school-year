uint64_t permute(uint64_t input, uint8_t *perm, uint8_t size){//prenarejda bitovete
    uint64_t output = 0;
    for(int i = 0; i < size; i++)  {
        uint64_t bit = (input >> (size - perm[i])) & 1;
        output |= (bit << (size - i - 1));
    }
    return output;
}

uint32_t func(uint32_t R, uint64_t key){//razshirqva
    uint64_t golqmo = permute((uint64_t)R, golemo, 48);
    uint64_t xored = golqmo ^ key;
    uint32_t result = 0;

    for(int i = 0; i < 8; i++){
        uint8_t segment = (xored >> (42 - 6 * i)) & 0x3F;

        uint8_t row = ((segment & 0x20) >> 4) | (segment & 0x01);
        uint8_t col = (segment >> 1) & 0x0F;

        uint8_t s_value = box[i][row][col];

        result |= s_value << (28 - 4 * i);
    }
    return result;
}

uint64_t encrypt_block(uint64_t block, uint64_t key){
    uint64_t permuted = permute(block, IP, 64);
    uint32_t L = permuted >> 32;
    uint32_t R = permuted & 0xFFFFFFFF;
    
    for(int i = 0; i < 16; i++){
        uint32_t temp = R;
        R = L ^ func(R, key);
        L = temp;
    }
    
    uint64_t preoutput = ((uint64_t)R << 32) | L;
    return permute(preoutput, ff, 64);
}

uint64_t des_decrypt_block(uint64_t block, uint64_t key) {
    uint64_t permuted = permute(block, IP, 64);
    
    uint32_t L = permuted >> 32;
    uint32_t R = permuted & 0xFFFFFFFF;
    
    for(int i = 15; i >= 0; i--) {
        uint32_t temp = R;
        R = L ^ func(R, key);
        L = temp;
    }
    
    uint64_t preoutput = ((uint64_t)R << 32) | L;
    
    return permute(preoutput, ff, 64);
}

